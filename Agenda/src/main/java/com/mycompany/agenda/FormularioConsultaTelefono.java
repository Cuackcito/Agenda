      String telefono = jTextField1.getText();

    ArrayList<Persona> resultados = ClaseControladora.buscarPorTelefono(telefono);

    DefaultListModel<String> modelo = new DefaultListModel<>();

    if (resultados.isEmpty()) {
    modelo.addElement("No existe registro con ese tel√©fono.");
    } else {
    for (Persona p : resultados) {
        modelo.addElement(p.mostrarCompleto());
    }
    }

jList1.setModel(modelo);
